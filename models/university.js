class University {
  constructor(
    id,
    name,
    department,
    score,
    upperScore,
    placement,
    isState,
    city,
    universityYear
  ) {
    this.id = id;
    this.name = name;
    this.department = department;
    this.score = score;
    this.upperScore = upperScore;
    this.placement = placement;
    this.isState = isState;
    this.city = city;
    this.universityYear = universityYear;
  }
}

export default University;
